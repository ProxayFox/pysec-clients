#!/usr/bin/env python3
"""MDE Arrow Schema Contract Builder

Reads   tests/mde_client/fixtures/mde_metadata.xml
Writes  src/mde_client/schemas/<type_snake>.py     (one file per top-level response type)
    src/mde_client/schemas/__init__.py         (re-exports everything, lazily)

Each generated schema file contains:
  - Named pa.StructType constants for every ComplexType dependency,
    topologically sorted so each constant is defined before it is referenced.
  - A single <ENTITY_UPPER>_SCHEMA: pa.Schema constant at the bottom.

The generated files match the hand-written style of schemas/machines.py:
  - Enums          → pa.string()                    (API returns human-readable strings)
  - DateTimeOffset → pa.timestamp("us", tz="UTC")   (microsecond precision, UTC-aware)
  - NavigationProperties excluded                    (relationship links, not columns)
  - Abstract ComplexTypes → pa.string()             (polymorphic, can't inline safely)

After writing all files, runs `just quality-schema` so the output is always
commit-ready — lint, format, typecheck, and tests all pass before exit.

Usage (from project root):
    just schema-build       # Build schemas if XML is stale or missing, then run quality gate
    just schema-build-dry   # Same, but print generated source to stdout instead of writing files
    just schema-refresh     # Fetch fresh $metadata from the API and write to XML_SOURCE, then exit (no generation)
    just schema-refresh-dry # Same, but print XML to stdout instead of writing file
"""

from __future__ import annotations

import argparse
import os
import re
import subprocess
import xml.etree.ElementTree as ET
from datetime import datetime, timedelta
from pathlib import Path

import pyarrow as pa

# ── Configuration ─────────────────────────────────────────────────────────────

XML_SOURCE = Path("tests/mde_client/fixtures/mde_metadata.xml")
OUTPUT_DIR = Path("src/mde_client/schemas")
METADATA_MAX_AGE = timedelta(days=7)

MDE_NS = "http://docs.oasis-open.org/odata/ns/edm"
MDE_API_PREFIX = "microsoft.windowsDefenderATP.api."

_GENERATED_HEADER = "# AUTO-GENERATED by scripts/mde_contract.py"

# OData primitive → (pa.DataType, source expression).
#
# The pa.DataType half is the single source of truth — ty/pyright validate it
# and the test validator uses it directly for comparisons.
# The str half is emitted verbatim into generated .py files by _type_expr().
# Keeping them together means they can never silently drift apart.
EDM_PA_TYPES: dict[str, tuple[pa.DataType, str]] = {
    "Edm.String": (pa.string(), "pa.string()"),
    "Edm.Int16": (pa.int16(), "pa.int16()"),
    "Edm.Int32": (pa.int32(), "pa.int32()"),
    "Edm.Int64": (pa.int64(), "pa.int64()"),
    "Edm.Boolean": (pa.bool_(), "pa.bool_()"),
    "Edm.Double": (pa.float64(), "pa.float64()"),
    "Edm.Single": (pa.float32(), "pa.float32()"),
    "Edm.Byte": (pa.uint8(), "pa.uint8()"),
    "Edm.SByte": (pa.int8(), "pa.int8()"),
    "Edm.Guid": (pa.string(), "pa.string()"),
    "Edm.DateTimeOffset": (
        pa.timestamp("us", tz="UTC"),
        'pa.timestamp("us", tz="UTC")',
    ),
    "Edm.Duration": (pa.duration("us"), 'pa.duration("us")'),
    "Edm.TimeOfDay": (pa.time64("us"), 'pa.time64("us")'),
    "Edm.Binary": (pa.large_binary(), "pa.large_binary()"),
}

# EntityTypes to skip — action parameter wrappers, not query-able resources
SKIP_ENTITIES: set[str] = {
    "PostDeviceGroupsRequest",
}

# Bound actions that omit ReturnType in the published metadata but still return
# contract-defined payload shapes at runtime. The override value is the XML
# type name that should be treated as the response root for schema generation.
NO_RETURN_ACTION_RESPONSE_OVERRIDES: dict[tuple[str, str], str] = {
    (
        "GetScanHistoryBySessionId",
        "Collection(microsoft.windowsDefenderATP.api.DeviceAuthenticatedScanDefinition)",
    ): "AuthScanHistoryContract",
    (
        "GetScanHistoryByScanDefinitionId",
        "Collection(microsoft.windowsDefenderATP.api.DeviceAuthenticatedScanDefinition)",
    ): "AuthScanHistoryContract",
}


# ── Naming helpers ────────────────────────────────────────────────────────────


def camel_to_snake(name: str) -> str:
    """MachineAction → machine_action"""
    s1 = re.sub(r"(.)([A-Z][a-z]+)", r"\1_\2", name)
    return re.sub(r"([a-z0-9])([A-Z])", r"\1_\2", s1).lower()


def to_const(name: str) -> str:
    """MachineAction → MACHINE_ACTION"""
    return camel_to_snake(name).upper()


def strip_prefix(odata_type: str) -> str:
    """'microsoft.windowsDefenderATP.api.Alert' → 'Alert'"""
    if odata_type.startswith(MDE_API_PREFIX):
        return odata_type[len(MDE_API_PREFIX) :]
    return odata_type


def unwrap_collection(odata_t: str) -> str:
    if odata_t.startswith("Collection("):
        return odata_t[len("Collection(") : -1]
    return odata_t


# ── Quality gate ──────────────────────────────────────────────────────────────


def run_quality() -> None:
    """Run `just quality-schema` so generated files are lint-, format-, and type-clean."""
    result = subprocess.run(["just", "quality-schema"])
    if result.returncode != 0:
        raise SystemExit(result.returncode)


# ── Metadata fetcher ──────────────────────────────────────────────────────────


def fetch_metadata() -> None:
    """Fetch fresh $metadata from the MDE API and write it to XML_SOURCE."""
    from mde_client import MDEClient

    client = MDEClient(
        tenant_id=os.environ["AZURE_TENANT_ID"],
        client_id=os.environ["AZURE_CLIENT_ID"],
        client_secret=os.environ["AZURE_CLIENT_SECRET"],
    )

    print("Fetching MDE $metadata from API...")
    response = client.misc._request("GET", "api/$metadata")
    response.raise_for_status()

    XML_SOURCE.parent.mkdir(parents=True, exist_ok=True)
    XML_SOURCE.write_bytes(response.content)
    print(f"  ✓ saved to {XML_SOURCE}")


def metadata_is_stale() -> bool:
    """True when XML_SOURCE is missing or older than METADATA_MAX_AGE."""
    if not XML_SOURCE.exists():
        return True
    age = datetime.now() - datetime.fromtimestamp(XML_SOURCE.stat().st_mtime)
    return age > METADATA_MAX_AGE


# ── Stale file cleanup ────────────────────────────────────────────────────────


def cleanup_stale_schemas(expected_paths: set[Path]) -> None:
    """Remove AUTO-GENERATED schema files that are no longer in the expected set.

    Only removes files whose first line contains the generated header — hand-written
    files (e.g. the old machines.py) are never touched.
    """
    for path in sorted(OUTPUT_DIR.glob("*.py")):
        if path.name == "__init__.py":
            continue
        if path in expected_paths:
            continue
        try:
            first_line = path.read_text(encoding="utf-8").split("\n", 1)[0]
        except OSError:
            continue
        if _GENERATED_HEADER in first_line:
            path.unlink()
            print(f"  🗑  removed stale {path.name}")


# ── Metadata parser ───────────────────────────────────────────────────────────


class MDEMetadata:
    """Parses the MDE OData CSDL XML into typed registries."""

    def __init__(self, path: Path) -> None:
        root = ET.parse(path).getroot()

        self.enum_types: set[str] = set()
        self.abstract_types: set[str] = set()
        # short name → list of <Property> elements (no <NavigationProperty>)
        self.complex_types: dict[str, list[ET.Element]] = {}
        self.entity_types: dict[str, list[ET.Element]] = {}
        self.response_complex_types: set[str] = set()
        # child short name → parent short name
        self.base_types: dict[str, str] = {}

        for el in root.iter(f"{{{MDE_NS}}}EnumType"):
            self.enum_types.add(el.get("Name", ""))

        for el in root.iter(f"{{{MDE_NS}}}ComplexType"):
            name = el.get("Name", "")
            self.complex_types[name] = el.findall(f"{{{MDE_NS}}}Property")
            if el.get("Abstract", "false").lower() == "true":
                self.abstract_types.add(name)
            if base := el.get("BaseType"):
                self.base_types[name] = strip_prefix(base)

        for el in root.iter(f"{{{MDE_NS}}}EntityType"):
            name = el.get("Name", "")
            # NavigationProperties intentionally excluded — relationship links
            # resolved server-side, not columns returned in the response body.
            self.entity_types[name] = el.findall(f"{{{MDE_NS}}}Property")
            if el.get("Abstract", "false").lower() == "true":
                self.abstract_types.add(name)
            if base := el.get("BaseType"):
                self.base_types[name] = strip_prefix(base)

        for kind in ("Function", "Action"):
            for el in root.iter(f"{{{MDE_NS}}}{kind}"):
                return_el = el.find(f"{{{MDE_NS}}}ReturnType")
                if return_el is None:
                    binding_el = el.find(
                        f"{{{MDE_NS}}}Parameter[@Name='bindingParameter']"
                    )
                    binding_type = (
                        binding_el.get("Type", "") if binding_el is not None else ""
                    )
                    override_key = (el.get("Name", ""), binding_type)
                    override_type = NO_RETURN_ACTION_RESPONSE_OVERRIDES.get(
                        override_key
                    )
                    if override_type is None:
                        continue
                    return_type = override_type
                else:
                    return_type = return_el.get("Type", "")

                short_name = strip_prefix(unwrap_collection(return_type))

                if (
                    short_name in self.complex_types
                    and short_name not in self.abstract_types
                ):
                    self.response_complex_types.add(short_name)

    def props_for(self, type_name: str) -> list[ET.Element]:
        """Return flattened (inherited → own) Property elements for any type name."""
        chain: list[str] = []
        cursor = type_name
        while parent := self.base_types.get(cursor):
            chain.append(parent)
            cursor = parent

        props: list[ET.Element] = []
        for ancestor in reversed(chain):
            props += (
                self.entity_types.get(ancestor)
                or self.complex_types.get(ancestor)
                or []
            )
        props += (
            self.entity_types.get(type_name) or self.complex_types.get(type_name) or []
        )
        return props

    def concrete_descendants(self, abstract_name: str) -> list[str]:
        """Return concrete ComplexType names that directly derive from *abstract_name*."""
        return [
            child
            for child, parent in self.base_types.items()
            if parent == abstract_name
            and child in self.complex_types
            and child not in self.abstract_types
        ]

    def _normalize_odata_for_compat(self, odata_t: str) -> str:
        """Normalize an OData type for Arrow-level compatibility comparison.

        Different enum type names all map to ``pa.string()``, so they are
        treated as equivalent for merge-safety checks.
        """
        if odata_t.startswith("Collection("):
            inner = odata_t[len("Collection(") : -1]
            return f"Collection({self._normalize_odata_for_compat(inner)})"
        short = strip_prefix(odata_t)
        if short in self.enum_types:
            return "Edm.String"
        return odata_t

    def can_merge_abstract(self, abstract_name: str) -> bool:
        """True when all concrete descendants share compatible field types.

        "Compatible" means every field name that appears in more than one
        descendant maps to the same Arrow type.  When this holds the
        descendants can be flattened into a single nullable Arrow struct.
        """
        descendants = self.concrete_descendants(abstract_name)
        if not descendants:
            return False

        field_types: dict[str, str] = {}
        for desc in descendants:
            for prop in self.props_for(desc):
                name = prop.get("Name", "")
                odata_t = self._normalize_odata_for_compat(prop.get("Type", ""))
                if name in field_types and field_types[name] != odata_t:
                    return False
                field_types[name] = odata_t
        return True

    def merged_abstract_props(self, abstract_name: str) -> list[ET.Element]:
        """Return the union of properties across base + all concrete descendants.

        Every field is made nullable in the merged view because any given
        response will only populate the subset for its concrete subtype.
        Ordering: base fields first, then descendant-only fields in
        discovery order.
        """
        seen_names: set[str] = set()
        merged: list[ET.Element] = []

        for prop in self.props_for(abstract_name):
            name = prop.get("Name", "")
            if name not in seen_names:
                seen_names.add(name)
                merged.append(prop)

        for desc in self.concrete_descendants(abstract_name):
            for prop in self.props_for(desc):
                name = prop.get("Name", "")
                if name not in seen_names:
                    seen_names.add(name)
                    merged.append(prop)

        return merged

    def referenced_complex_types(
        self, type_name: str, _seen: set[str] | None = None
    ) -> list[str]:
        """Return all ComplexType short names transitively reachable from type_name.

        Returns in dependency order so constants can be emitted without
        forward-reference issues.  Mergeable abstract types and their
        concrete descendants are included so the emitter can produce the
        merged struct constant.
        """
        if _seen is None:
            _seen = set()
        result: list[str] = []
        for prop in self.props_for(type_name):
            raw = prop.get("Type", "")
            short = strip_prefix(unwrap_collection(raw))
            if short in self.complex_types and short not in _seen:
                _seen.add(short)
                # If abstract and mergeable, pull in descendants too
                if short in self.abstract_types and self.can_merge_abstract(short):
                    for desc in self.concrete_descendants(short):
                        if desc not in _seen:
                            _seen.add(desc)
                            result.extend(self.referenced_complex_types(desc, _seen))
                result.extend(self.referenced_complex_types(short, _seen))
                result.append(short)
        return result


# ── Code generator ────────────────────────────────────────────────────────────


class SchemaCodeGen:
    def __init__(self, meta: MDEMetadata) -> None:
        self.meta = meta

    # -- Type expression -------------------------------------------------------

    def _type_expr(self, odata_t: str) -> str:
        """Resolve an OData type string to a PyArrow source expression string."""
        if odata_t.startswith("Collection("):
            inner = odata_t[len("Collection(") : -1]
            return f"pa.list_({self._type_expr(inner)})"

        if odata_t in EDM_PA_TYPES:
            _, expr = EDM_PA_TYPES[odata_t]
            return expr

        short = strip_prefix(odata_t)

        if short in self.meta.enum_types:
            return "pa.string()"

        if short in self.meta.complex_types:
            if short in self.meta.abstract_types:
                if self.meta.can_merge_abstract(short):
                    return f"{to_const(short)}_TYPE"
                return "pa.string()"
            return f"{to_const(short)}_TYPE"

        return "pa.string()"

    # -- Field line ------------------------------------------------------------

    def _field_line(self, prop: ET.Element, indent: int = 8) -> str:
        name = prop.get("Name", "")
        odata_t = prop.get("Type", "Edm.String")
        nullable = prop.get("Nullable", "true").lower() != "false"
        pad = " " * indent
        type_expr = self._type_expr(odata_t)
        if nullable:
            return f'{pad}pa.field("{name}", {type_expr}),'
        return f'{pad}pa.field("{name}", {type_expr}, nullable=False),'

    # -- Struct constant block -------------------------------------------------

    def _struct_block(self, ct_name: str) -> str:
        const = f"{to_const(ct_name)}_TYPE"
        lines = [f"{const}: pa.StructType = pa.struct("]
        lines += ["    ["]

        if ct_name in self.meta.abstract_types and self.meta.can_merge_abstract(
            ct_name
        ):
            # Merged abstract struct: union of base + descendant fields, all nullable
            for prop in self.meta.merged_abstract_props(ct_name):
                name = prop.get("Name", "")
                odata_t = prop.get("Type", "Edm.String")
                pad = " " * 8
                type_expr = self._type_expr(odata_t)
                lines.append(f'{pad}pa.field("{name}", {type_expr}),')
        else:
            for prop in self.meta.props_for(ct_name):
                lines.append(self._field_line(prop, indent=8))

        lines += ["    ]", ")"]
        return "\n".join(lines)

    # -- Schema constant block -------------------------------------------------

    def _schema_block(self, type_name: str) -> str:
        const = f"{to_const(type_name)}_SCHEMA"
        lines = [f"{const}: pa.Schema = pa.schema("]
        lines += ["    ["]
        for prop in self.meta.props_for(type_name):
            lines.append(self._field_line(prop, indent=8))
        lines += ["    ]", ")"]
        return "\n".join(lines)

    # -- Schema file -----------------------------------------------------------

    def generate(self, type_name: str) -> str:
        """Generate the full source for one schema .py file."""
        dep_structs = self.meta.referenced_complex_types(type_name)

        header_lines = [
            "# AUTO-GENERATED by scripts/mde_contract.py — do not edit manually.",
            "# Re-run the script to regenerate when MDE $metadata changes.",
            "import pyarrow as pa",
        ]

        parts: list[str] = ["\n".join(header_lines), ""]

        if dep_structs:
            parts.append("\n\n".join(self._struct_block(ct) for ct in dep_structs))
            parts.append("")

        parts.append(self._schema_block(type_name))
        parts.append("")  # trailing newline

        return "\n".join(parts)

    # -- __init__.py -----------------------------------------------------------

    def _exports_for(self, type_name: str) -> list[str]:
        """_SCHEMA first, then non-abstract _TYPE deps in topological order.

        Mergeable abstract types are also exported since they produce a
        concrete struct constant.
        """
        exports: list[str] = [f"{to_const(type_name)}_SCHEMA"]
        seen: set[str] = {exports[0]}
        for ct_name in self.meta.referenced_complex_types(type_name):
            if ct_name not in self.meta.abstract_types or self.meta.can_merge_abstract(
                ct_name
            ):
                const = f"{to_const(ct_name)}_TYPE"
                if const not in seen:
                    seen.add(const)
                    exports.append(const)
        return exports

    def generate_init(self, type_names: list[str]) -> str:
        """Generate schemas/__init__.py with PEP 562 lazy loading."""
        name_to_module: dict[str, str] = {}
        for type_name in type_names:
            module = camel_to_snake(type_name)
            for export in self._exports_for(type_name):
                if export not in name_to_module:
                    name_to_module[export] = module

        seen_modules: dict[str, list[str]] = {}
        for name, module in name_to_module.items():
            seen_modules.setdefault(module, []).append(name)

        type_checking_lines: list[str] = []
        for module, names in seen_modules.items():
            if len(names) == 1:
                type_checking_lines.append(f"    from .{module} import {names[0]}")
            else:
                joined = ",\n        ".join(names)
                type_checking_lines.append(
                    f"    from .{module} import (\n        {joined},\n    )"
                )

        registry_lines: list[str] = ["_NAME_TO_MODULE: dict[str, str] = {"]
        for name, module in name_to_module.items():
            registry_lines.append(f'    "{name}": "{module}",')
        registry_lines.append("}")

        all_entries = ",\n    ".join(f'"{n}"' for n in sorted(name_to_module))

        lines: list[str] = [
            "# AUTO-GENERATED by scripts/mde_contract.py — do not edit manually.",
            "# Re-run the script to regenerate when MDE $metadata changes.",
            "from __future__ import annotations",
            "",
            "import importlib",
            "from typing import TYPE_CHECKING",
            "",
            "if TYPE_CHECKING:",
            *type_checking_lines,
            "",
            *registry_lines,
            "",
            "",
            "def __getattr__(name: str) -> object:",
            '    """Lazily import schema constants on first access (PEP 562).',
            "",
            "    The resolved value is written back into the module's globals so",
            "    subsequent accesses skip this function entirely.",
            '    """',
            "    module_name = _NAME_TO_MODULE.get(name)",
            "    if module_name is None:",
            '        raise AttributeError(f"module {__name__!r} has no attribute {name!r}")',
            "    module = importlib.import_module(f'.{module_name}', __name__)",
            "    value = getattr(module, name)",
            "    globals()[name] = value  # cache: subsequent access bypasses __getattr__",
            "    return value",
            "",
            "",
            "__all__ = [",
            f"    {all_entries},",
            "]",
            "",
        ]

        return "\n".join(lines)


# ── CLI ───────────────────────────────────────────────────────────────────────


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Generate PyArrow schema files from MDE $metadata XML."
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Print generated source to stdout instead of writing files.",
    )
    parser.add_argument(
        "--entity",
        metavar="NAME",
        help="Generate only the schema for this EntityType (e.g. 'Alert').",
    )

    fetch_group = parser.add_mutually_exclusive_group()
    fetch_group.add_argument(
        "--refresh-metadata",
        action="store_true",
        help="Force re-fetch of $metadata from the API before generating schemas.",
    )
    fetch_group.add_argument(
        "--no-fetch",
        action="store_true",
        help="Skip the staleness check and generate from existing XML as-is. "
        "Useful when credentials are unavailable locally.",
    )
    args = parser.parse_args()

    # ── Metadata freshness check ──────────────────────────────────────────────

    if args.no_fetch:
        if not XML_SOURCE.exists():
            raise FileNotFoundError(
                f"{XML_SOURCE} not found. Cannot use --no-fetch without existing XML. "
                "Run without --no-fetch to fetch it first."
            )
        print(f"  Using existing {XML_SOURCE} (--no-fetch)")
    elif args.refresh_metadata or metadata_is_stale():
        if not all(
            k in os.environ
            for k in ("AZURE_TENANT_ID", "AZURE_CLIENT_ID", "AZURE_CLIENT_SECRET")
        ):
            raise EnvironmentError(
                "AZURE_TENANT_ID, AZURE_CLIENT_ID, and AZURE_CLIENT_SECRET must be "
                "set to fetch metadata from the MDE API.\n"
                "To generate from existing XML without credentials, use --no-fetch."
            )
        fetch_metadata()

    if not XML_SOURCE.exists():
        raise FileNotFoundError(
            f"{XML_SOURCE} not found. Run with --refresh-metadata to fetch it."
        )

    # ── Parse and generate ────────────────────────────────────────────────────

    meta = MDEMetadata(XML_SOURCE)
    gen = SchemaCodeGen(meta)

    available_schema_types = sorted(
        (set(meta.entity_types) - SKIP_ENTITIES) | meta.response_complex_types
    )

    if args.entity and args.entity not in available_schema_types:
        available = ", ".join(available_schema_types)
        raise ValueError(f"Unknown entity {args.entity!r}.\nAvailable: {available}")

    all_schema_types = available_schema_types
    types_to_generate = (
        sorted({args.entity, *meta.response_complex_types})
        if args.entity
        else all_schema_types
    )

    if not args.dry_run:
        OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

    # ── Per-entity schema files ───────────────────────────────────────────────

    expected_paths: set[Path] = set()
    count = 0

    for type_name in types_to_generate:
        source = gen.generate(type_name)
        out_path = OUTPUT_DIR / (camel_to_snake(type_name) + ".py")
        expected_paths.add(out_path)

        if args.dry_run:
            sep = "─" * 60
            print(f"\n{sep}\n# {out_path}\n{sep}")
            print(source)
        else:
            out_path.write_text(source, encoding="utf-8")
            print(f"  ✓ {out_path}")

        count += 1

    # ── __init__.py ───────────────────────────────────────────────────────────

    init_source = gen.generate_init(all_schema_types)
    init_path = OUTPUT_DIR / "__init__.py"

    if args.dry_run:
        sep = "─" * 60
        print(f"\n{sep}\n# {init_path}\n{sep}")
        print(init_source)
    else:
        init_path.write_text(init_source, encoding="utf-8")
        print(f"  ✓ {init_path}")

    # ── Stale file cleanup ────────────────────────────────────────────────────
    # Only on full runs (not --entity) so we don't remove files we didn't intend
    # to touch. Skipped in dry-run mode.

    if not args.entity and not args.dry_run:
        cleanup_stale_schemas(expected_paths)

    # ── Quality gate ──────────────────────────────────────────────────────────

    if not args.dry_run:
        print(f"\n{count} schema file(s) + __init__.py written to {OUTPUT_DIR}/")
        run_quality()


if __name__ == "__main__":
    main()
